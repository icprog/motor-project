void MotorControlInit () {
  TactInit();
}

void MotorControlUpdate () {
  if (GetTimeSinceTick() > 4000) LedSet(LED_BLUE, true);
  
  LcdSetPos(0,0);
  LcdPrintf("%d \n", (int)GetRPM());
}

